-- SECCIÓN 26. FUNCIÓN Y TRIGGER PARA AUTO-CREACIÓN DE PARTICIONES
-- ============================================================================

CREATE OR REPLACE FUNCTION ruta.create_client_partitions(p_client_id BIGINT)
RETURNS VOID AS $$
DECLARE
  partitioned_tables TEXT[] := ARRAY[
    'users', 'buyer_profiles', 'courier_profiles',
    'client_payment_providers',
    'pickup_points', 'product_categories', 'products',
    'recurrence_templates', 'orders', 'order_items',
    'order_state_history', 'payments', 'refunds',
    'returns', 'disputes', 'webhook_deliveries',
    'audit_events', 'idempotency_keys',
    'client_parameters', 'operator_permissions'
  ];
  t TEXT;
BEGIN
  FOREACH t IN ARRAY partitioned_tables LOOP
    BEGIN
      EXECUTE format(
        'CREATE TABLE IF NOT EXISTS ruta.%I_p%s PARTITION OF ruta.%I FOR VALUES IN (%L)',
        t, p_client_id, t, p_client_id
      );
    EXCEPTION
      WHEN undefined_table THEN
        -- La tabla padre aún no existe. Esto solo debe ocurrir durante el
        -- bootstrap del schema, cuando se inserta el Cliente plataforma (id=0)
        -- antes de que client_parameters y operator_permissions hayan sido
        -- creadas (ver SECCIÓN 33B). Las particiones para esas tablas se
        -- crean manualmente al final de la SECCIÓN 33B. Para cualquier otro
        -- Cliente nuevo todas las tablas ya existen y esta rama no se ejecuta.
        NULL;
    END;
  END LOOP;
END;
$$ LANGUAGE plpgsql;

COMMENT ON FUNCTION ruta.create_client_partitions IS
  'Crea todas las particiones para un client_id dado. Se ejecuta
  automáticamente al insertar en clients vía trigger. Resiliente a tablas
  padre faltantes (solo durante bootstrap del cliente plataforma).';


CREATE OR REPLACE FUNCTION ruta.trg_auto_create_partitions()
RETURNS TRIGGER AS $$
BEGIN
  PERFORM ruta.create_client_partitions(NEW.id);
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_clients_auto_partitions
AFTER INSERT ON ruta.clients
FOR EACH ROW
EXECUTE FUNCTION ruta.trg_auto_create_partitions();


