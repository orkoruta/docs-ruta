-- ============================================================================
-- SECCIÓN 12. RECURRENCIA (plantillas de pedidos recurrentes)
-- ============================================================================

CREATE TABLE ruta.recurrence_templates (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  client_id BIGINT NOT NULL,
  buyer_id BIGINT NOT NULL,
  recurrence_mode TEXT NOT NULL
    CHECK (recurrence_mode IN ('SCHEDULED_RECURRING', 'REPEAT_LAST_ORDER')),
  recurrence_periodicity TEXT
    CHECK (recurrence_periodicity IS NULL OR recurrence_periodicity IN (
      'DAILY', 'WEEKLY', 'BIWEEKLY', 'MONTHLY', 'CUSTOM_INTERVAL'
    )),
  custom_interval_days INT,
  recurrence_status TEXT NOT NULL DEFAULT 'RECURRENCE_ACTIVE'
    CHECK (recurrence_status IN (
      'RECURRENCE_ACTIVE', 'RECURRENCE_PAUSED', 'RECURRENCE_CANCELLED'
    )),

  delivery_type TEXT NOT NULL
    CHECK (delivery_type IN ('SHIP', 'PICKUP')),
  delivery_carrier_type TEXT
    CHECK (delivery_carrier_type IS NULL OR delivery_carrier_type IN (
      'OWN_FLEET', 'EXTERNAL_COURIER'
    )),
  payment_method TEXT NOT NULL
    CHECK (payment_method IN (
      'ONLINE_AT_ORDER', 'ELECTRONIC_ON_DELIVERY', 'CASH_ON_DELIVERY'
    )),
  payment_method_submethod TEXT,
  payment_provider_id BIGINT,
  pickup_point_id BIGINT,

  template_payload JSONB NOT NULL,
  last_generated_at TIMESTAMPTZ,
  next_generation_at TIMESTAMPTZ,

  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

  PRIMARY KEY (id, client_id),
  CONSTRAINT fk_rt_client FOREIGN KEY (client_id) REFERENCES ruta.clients(id),
  CONSTRAINT fk_rt_buyer FOREIGN KEY (buyer_id, client_id)
    REFERENCES ruta.users(id, client_id),
  CONSTRAINT fk_rt_payment_provider FOREIGN KEY (payment_provider_id, client_id)
    REFERENCES ruta.client_payment_providers(id, client_id),
  CONSTRAINT fk_rt_pickup_point FOREIGN KEY (pickup_point_id, client_id)
    REFERENCES ruta.pickup_points(id, client_id)
) PARTITION BY LIST (client_id);


-- ============================================================================
-- SECCIÓN 13. TABLA PRINCIPAL: orders
-- ============================================================================

CREATE TABLE ruta.orders (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  client_id BIGINT NOT NULL,
  buyer_id BIGINT NOT NULL,
  courier_user_id BIGINT,

  -- Origen del pedido
  order_origin TEXT NOT NULL
    CHECK (order_origin IN (
      'BUYER_UI',
      'CORPORATE_MANUAL',
      'RECURRENCE',
      'FULL_LANDING_API',
      'API_LOGISTICS'
    )),

  -- Estados separados por dominio
  order_status TEXT NOT NULL
    CHECK (order_status IN (
      'DRAFT', 'PENDING_CONFIRM', 'ORDER_SUBMITTED', 'EXPIRED',
      'ORDER_VALIDATING', 'MANUAL_REVIEW', 'VALIDATION_APPROVED',
      'VALIDATION_REJECTED', 'SELLER_CONFIRMED', 'PREPARING',
      'AWAITING_COURIER_ASSIGNMENT', 'COURIER_ASSIGNED',
      'READY_TO_SHIP', 'READY_FOR_PICKUP',
      'SHIPMENT_HOLD', 'SHIPPED', 'IN_TRANSIT', 'ON_HOLD',
      'OUT_FOR_DELIVERY', 'ARRIVED_AT_CUSTOMER',
      'DELIVERY_ATTEMPTED', 'DELIVERY_RESCHEDULED', 'LOST_IN_TRANSIT',
      'AT_PICKUP_POINT', 'CUSTOMER_ARRIVED_AT_PICKUP_POINT',
      'IDENTITY_VALIDATED', 'PICKUP_AUTH_FAILED', 'PICKUP_POINT_ISSUE',
      'PICKUP_EXPIRED', 'PICKUP_CANCELLED_BY_CUSTOMER', 'PICKED_UP',
      'PAYMENT_COLLECTION_PENDING', 'PAYMENT_COLLECTED_ELECTRONIC',
      'PAYMENT_COLLECTED_CASH', 'CASH_COLLECTION_PENDING',
      'CASH_PAYMENT_REJECTED',
      'CANCELLED_BY_CUSTOMER', 'CANCELLED_BY_SELLER',
      'CANCELLED_BY_SYSTEM', 'CANCELLED_BY_ADMIN', 'CANCELLED_NO_PAYMENT',
      'CUSTOMER_CANCEL_REQUEST', 'CANCEL_REQUEST_APPROVED',
      'CANCEL_REQUEST_REJECTED',
      'RETURN_TO_ORIGIN', 'RETURN_TO_ORIGIN_RECEIVED', 'LOST_IN_RETURN',
      'DELIVERED', 'DELIVERY_DISPUTED', 'CONFIRMED_BY_CUSTOMER',
      'CONFIRMED_BY_SYSTEM', 'COMPLETED_SUCCESSFULLY', 'CLOSED'
    )),

  payment_status TEXT NOT NULL
    CHECK (payment_status IN (
      'PAYMENT_NOT_STARTED', 'PENDING_ONLINE_PAYMENT',
      'PAYMENT_PROCESSING', 'PAID',
      'PAYMENT_FAILED_RETRYABLE', 'PAYMENT_REJECTED_FINAL',
      'PENDING_COLLECTION', 'COLLECTION_PROCESSING',
      'PAYMENT_COLLECTED', 'PAYMENT_COLLECTION_FAILED',
      'PAYMENT_NOT_COLLECTED'
    )),

  refund_status TEXT NOT NULL DEFAULT 'REFUND_NOT_REQUIRED'
    CHECK (refund_status IN (
      'REFUND_NOT_REQUIRED', 'REFUND_PENDING', 'REFUND_PROCESSING',
      'REFUND_PROVIDER_REQUESTED', 'REFUNDED', 'PARTIALLY_REFUNDED',
      'REFUND_FAILED'
    )),

  return_status TEXT
    CHECK (return_status IS NULL OR return_status IN (
      'RETURN_REQUESTED', 'RETURN_UNDER_REVIEW', 'RETURN_APPROVED',
      'RETURN_REJECTED', 'RETURN_CANCELLED',
      'CUSTOMER_RETURN_IN_TRANSIT', 'CUSTOMER_RETURN_EXPIRED',
      'CUSTOMER_RETURN_RECEIVED', 'CUSTOMER_RETURN_LOST',
      'PICKUP_SCHEDULED', 'PICKUP_OUT_FOR_COLLECTION',
      'PICKUP_COLLECTED', 'PICKUP_FAILED'
    )),

  dispute_status TEXT
    CHECK (dispute_status IS NULL OR dispute_status IN (
      'DISPUTED', 'DISPUTE_RESOLVED'
    )),

  -- Atributos de bifurcación (BLOQUE 0 de los flujos)
  delivery_type TEXT NOT NULL CHECK (delivery_type IN ('SHIP', 'PICKUP')),
  delivery_carrier_type TEXT
    CHECK (delivery_carrier_type IS NULL OR delivery_carrier_type IN (
      'OWN_FLEET', 'EXTERNAL_COURIER'
    )),

  payment_method TEXT NOT NULL
    CHECK (payment_method IN (
      'ONLINE_AT_ORDER',
      'ELECTRONIC_ON_DELIVERY',
      'CASH_ON_DELIVERY',
      'EXTERNAL_PREPAID'
    )),
  payment_method_submethod TEXT
    CHECK (payment_method_submethod IS NULL OR payment_method_submethod IN (
      'DATAFONO', 'BANK_TRANSFER', 'PAYMENT_LINK', 'QR_OR_PAYMENT_LINK'
    )),

  refund_modality TEXT
    CHECK (refund_modality IS NULL OR refund_modality IN (
      'STORE_CREDIT', 'BANK_REFUND'
    )),
  return_mechanism TEXT
    CHECK (return_mechanism IS NULL OR return_mechanism IN (
      'BUYER_SHIPS_VIA_COURIER', 'CLIENT_PICKS_UP'
    )),

  buyer_type TEXT NOT NULL DEFAULT 'INDIVIDUAL'
    CHECK (buyer_type IN ('INDIVIDUAL', 'CORPORATE')),

  closure_reason TEXT
    CHECK (closure_reason IS NULL OR closure_reason IN (
      'COMPLETED_SUCCESSFULLY', 'CANCELLED_BY_CUSTOMER', 'CANCELLED_BY_SELLER',
      'CANCELLED_BY_SYSTEM', 'CANCELLED_BY_ADMIN', 'CANCELLED_NO_PAYMENT',
      'PAYMENT_TIMEOUT', 'PAYMENT_REJECTED', 'VALIDATION_REJECTED',
      'DELIVERY_FAILED', 'PICKUP_EXPIRED', 'LOST_IN_TRANSIT',
      'LOST_IN_RETURN', 'EXPIRED', 'CLIENT_DEACTIVATED'
    )),

  -- Referencias
  payment_provider_id BIGINT,
  external_payment_reference TEXT,
  recurrence_template_id BIGINT,

  -- Dirección de entrega (para SHIP)
  delivery_address_line TEXT,
  delivery_address_city TEXT,
  delivery_address_state TEXT,
  delivery_address_country TEXT,
  delivery_address_postal_code TEXT,
  delivery_address_latitude NUMERIC(10,7),
  delivery_address_longitude NUMERIC(10,7),
  delivery_instructions TEXT,

  -- Punto de recogida (para PICKUP)
  pickup_point_id BIGINT,

  -- Financiero
  subtotal NUMERIC(12,2) NOT NULL DEFAULT 0,
  tax NUMERIC(12,2) NOT NULL DEFAULT 0,
  shipping_fee NUMERIC(12,2) NOT NULL DEFAULT 0,
  discount NUMERIC(12,2) NOT NULL DEFAULT 0,
  total NUMERIC(12,2) NOT NULL DEFAULT 0,
  currency TEXT NOT NULL DEFAULT 'COP',

  -- Concurrencia
  version BIGINT NOT NULL DEFAULT 1,

  -- Timestamps
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  submitted_at TIMESTAMPTZ,
  delivered_at TIMESTAMPTZ,
  closed_at TIMESTAMPTZ,
  archived BOOLEAN NOT NULL DEFAULT FALSE,
  archived_at TIMESTAMPTZ,

  PRIMARY KEY (id, client_id),
  CONSTRAINT fk_orders_client FOREIGN KEY (client_id) REFERENCES ruta.clients(id),
  CONSTRAINT fk_orders_buyer FOREIGN KEY (buyer_id, client_id)
    REFERENCES ruta.users(id, client_id),
  CONSTRAINT fk_orders_courier FOREIGN KEY (courier_user_id, client_id)
    REFERENCES ruta.users(id, client_id),
  CONSTRAINT fk_orders_payment_provider FOREIGN KEY (payment_provider_id, client_id)
    REFERENCES ruta.client_payment_providers(id, client_id),
  CONSTRAINT fk_orders_pickup_point FOREIGN KEY (pickup_point_id, client_id)
    REFERENCES ruta.pickup_points(id, client_id),
  CONSTRAINT fk_orders_recurrence FOREIGN KEY (recurrence_template_id, client_id)
    REFERENCES ruta.recurrence_templates(id, client_id),

  -- Coherencia: EXTERNAL_PREPAID solo aplica para API_LOGISTICS
  CONSTRAINT chk_orders_prepaid_origin CHECK (
    payment_method != 'EXTERNAL_PREPAID' OR order_origin = 'API_LOGISTICS'
  ),
  -- Coherencia: SHIP no usa pickup_point; PICKUP sí
  CONSTRAINT chk_orders_delivery_pickup CHECK (
    (delivery_type = 'SHIP' AND pickup_point_id IS NULL) OR
    (delivery_type = 'PICKUP')
  )
) PARTITION BY LIST (client_id);


