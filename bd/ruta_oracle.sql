-- ============================================================================
-- SECCIÓN ADICIONAL: client_parameters y operator_permissions
-- ============================================================================

CREATE TABLE client_parameters (
  id                              NUMBER(19) GENERATED BY DEFAULT AS IDENTITY,
  client_id                       NUMBER(19) NOT NULL,
  parameter_key                   VARCHAR2(200) NOT NULL,
  parameter_value                 VARCHAR2(4000) NOT NULL,
  value_type                      VARCHAR2(20) NOT NULL,
  description                     VARCHAR2(1000),
  is_overrideable_by_client       CHAR(1) DEFAULT 'Y' NOT NULL,
  created_at                      TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
  updated_at                      TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,

  CONSTRAINT pk_client_parameters PRIMARY KEY (id, client_id),
  CONSTRAINT uk_cparam_client_key UNIQUE (client_id, parameter_key),
  CONSTRAINT fk_cparam_client FOREIGN KEY (client_id) REFERENCES clients(id),
  CONSTRAINT chk_cparam_value_type CHECK (
    value_type IN ('INTEGER', 'DECIMAL', 'STRING', 'BOOLEAN', 'JSON')
  ),
  CONSTRAINT chk_cparam_overrideable CHECK (is_overrideable_by_client IN ('Y', 'N'))
);

COMMENT ON TABLE client_parameters IS
  'Parametros operativos. Valores globales en client_id=0; los Clientes pueden override.';


CREATE TABLE operator_permissions (
  operator_user_id      NUMBER(19) NOT NULL,
  client_id             NUMBER(19) NOT NULL,
  permission_key        VARCHAR2(100) NOT NULL,
  granted_at            TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
  granted_by_user_id    NUMBER(19) NOT NULL,

  CONSTRAINT pk_operator_permissions PRIMARY KEY (operator_user_id, client_id, permission_key),
  CONSTRAINT fk_op_user FOREIGN KEY (operator_user_id, client_id)
    REFERENCES users(id, client_id),
  CONSTRAINT fk_op_granter FOREIGN KEY (granted_by_user_id, client_id)
    REFERENCES users(id, client_id)
);

COMMENT ON TABLE operator_permissions IS
  'Permisos granulares por OPERATOR_CLIENT. ADMIN_CLIENT y ADMIN_RUTA tienen todos implicitamente.';


CREATE INDEX idx_cparam_client_key ON client_parameters (client_id, parameter_key);
CREATE INDEX idx_op_perm_user ON operator_permissions (client_id, operator_user_id);


