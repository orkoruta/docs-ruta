-- Verificar que el cliente plataforma existe y tiene sus particiones
DO $$
DECLARE
  partition_count INT;
BEGIN
  SELECT COUNT(*) INTO partition_count
  FROM pg_class c
  JOIN pg_namespace n ON n.oid = c.relnamespace
  WHERE n.nspname = 'ruta'
    AND c.relname LIKE '%_p0'
    AND c.relkind = 'r';

  IF partition_count < 20 THEN
    RAISE WARNING 'Solo se crearon % particiones para el cliente plataforma. Esperadas: al menos 20.', partition_count;
  END IF;
END
$$;

