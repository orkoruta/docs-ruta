CREATE OR REPLACE FUNCTION ruta.trg_log_order_state_change()
RETURNS TRIGGER AS $$
BEGIN
  -- order_status
  IF NEW.order_status IS DISTINCT FROM OLD.order_status THEN
    INSERT INTO ruta.order_state_history (
      client_id, order_id, state_dimension, previous_value, new_value
    ) VALUES (
      NEW.client_id, NEW.id, 'order_status', OLD.order_status, NEW.order_status
    );
  END IF;

  -- payment_status
  IF NEW.payment_status IS DISTINCT FROM OLD.payment_status THEN
    INSERT INTO ruta.order_state_history (
      client_id, order_id, state_dimension, previous_value, new_value
    ) VALUES (
      NEW.client_id, NEW.id, 'payment_status', OLD.payment_status, NEW.payment_status
    );
  END IF;

  -- refund_status
  IF NEW.refund_status IS DISTINCT FROM OLD.refund_status THEN
    INSERT INTO ruta.order_state_history (
      client_id, order_id, state_dimension, previous_value, new_value
    ) VALUES (
      NEW.client_id, NEW.id, 'refund_status', OLD.refund_status, NEW.refund_status
    );
  END IF;

  -- return_status
  IF NEW.return_status IS DISTINCT FROM OLD.return_status THEN
    INSERT INTO ruta.order_state_history (
      client_id, order_id, state_dimension, previous_value, new_value
    ) VALUES (
      NEW.client_id, NEW.id, 'return_status', OLD.return_status, NEW.return_status
    );
  END IF;

  -- dispute_status
  IF NEW.dispute_status IS DISTINCT FROM OLD.dispute_status THEN
    INSERT INTO ruta.order_state_history (
      client_id, order_id, state_dimension, previous_value, new_value
    ) VALUES (
      NEW.client_id, NEW.id, 'dispute_status', OLD.dispute_status, NEW.dispute_status
    );
  END IF;

  -- Incrementar version
  NEW.version = OLD.version + 1;

  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_orders_log_state
BEFORE UPDATE ON ruta.orders
FOR EACH ROW
