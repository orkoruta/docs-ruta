-- ============================================================================
-- RUTA — MODELO DE DATOS POSTGRESQL
-- ============================================================================
-- Plataforma multi-tenant para administración de operaciones comerciales
-- entre Clientes y Compradores.
--
-- Versión: 1.0
-- Schema: ruta
-- Base: PostgreSQL 14+
--
-- Este archivo crea el modelo de datos completo. Es ejecutable directo
-- sobre una base PostgreSQL limpia y reproduce toda la estructura
-- definida en docs/arquitectura/estrategia_multi_tenant_ruta.md y en
-- los 7 archivos de flujo (docs/flujos/flujo_*.txt).
--
-- Decisiones arquitectónicas reflejadas:
--   - Multi-tenant por client_id (BIGINT, no UUID).
--   - Particionamiento LIST por client_id con creación automática.
--   - Row Level Security activo en tablas operativas.
--   - Tablas append-only para auditoría e historial de estados.
--   - Soporte para Cliente API (Tipo A, solo logística) y Cliente Full
--     (Tipo B, ciclo completo).
--   - RUTA no custodia dinero: estados financieros para trazabilidad
--     y conciliación del Cliente.
-- ============================================================================

BEGIN;

-- ============================================================================
-- SECCIÓN 1. SCHEMA Y EXTENSIONES
-- ============================================================================

CREATE SCHEMA IF NOT EXISTS ruta;

SET search_path TO ruta, public;

-- Extensiones instaladas en schema 'public' (estándar). Si se instalan en
-- 'ruta', luego GRANT EXECUTE ON ALL FUNCTIONS IN SCHEMA ruta intentaría
-- otorgar permisos sobre funciones del superuser (pgcrypto, btree_gin) y
-- generaría cientos de WARNINGs inofensivos pero ruidosos.
CREATE EXTENSION IF NOT EXISTS pgcrypto WITH SCHEMA public;
CREATE EXTENSION IF NOT EXISTS btree_gin WITH SCHEMA public;


-- ============================================================================
-- SECCIÓN 2. ROLES DE BASE DE DATOS
-- ============================================================================

DO $do$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = 'ruta_app') THEN
    CREATE ROLE ruta_app NOLOGIN;
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = 'ruta_jobs') THEN
    CREATE ROLE ruta_jobs NOLOGIN BYPASSRLS;
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = 'ruta_readonly') THEN
    CREATE ROLE ruta_readonly NOLOGIN;
  END IF;
END
$do$;


-- ============================================================================
-- SECCIÓN 3. FUNCIONES AUXILIARES
-- ============================================================================

-- Trigger genérico: actualiza updated_at automáticamente
CREATE OR REPLACE FUNCTION ruta.trg_set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;


-- ============================================================================
-- SECCIÓN 4. TABLA: clients
-- No particionada. Tabla central de tenants.
-- ============================================================================

CREATE TABLE ruta.clients (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  business_code TEXT UNIQUE NOT NULL,
  slug TEXT UNIQUE NOT NULL,
  name TEXT NOT NULL,
  description TEXT,
  contact_person_name TEXT,
  contact_person_document_type TEXT,
  contact_person_document_number TEXT,
  contact_person_phone TEXT,
  contact_person_email TEXT,
  logo_url TEXT,

  client_type TEXT NOT NULL
    CHECK (client_type IN ('API', 'FULL')),

  frontend_mode TEXT
    CHECK (frontend_mode IS NULL OR frontend_mode IN (
      'NATIVE_RUTA', 'CUSTOM_LANDING_BY_RUTA'
    )),

  status TEXT NOT NULL DEFAULT 'ACTIVE'
    CHECK (status IN ('ACTIVE', 'INACTIVE')),

  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

  CONSTRAINT chk_clients_frontend_mode_type CHECK (
    (client_type = 'API' AND frontend_mode IS NULL) OR
    (client_type = 'FULL' AND frontend_mode IS NOT NULL)
  )
);

COMMENT ON TABLE ruta.clients IS
  'Tabla central de tenants. client_id = 0 representa la plataforma RUTA.';

CREATE TRIGGER trg_clients_updated_at
BEFORE UPDATE ON ruta.clients
FOR EACH ROW EXECUTE FUNCTION ruta.trg_set_updated_at();


-- ============================================================================
-- SECCIÓN 5. TABLA: users
-- Particionada por client_id. Tabla única para todos los usuarios.
-- ============================================================================

CREATE TABLE ruta.users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  client_id BIGINT NOT NULL,
  user_type TEXT NOT NULL
    CHECK (user_type IN (
      'ADMIN_RUTA',
      'ADMIN_CLIENT',
      'OPERATOR_CLIENT',
      'BUYER',
      'COURIER'
    )),

  email TEXT NOT NULL,
  password_hash TEXT,

  auth_mode TEXT NOT NULL DEFAULT 'PASSWORD'
    CHECK (auth_mode IN ('PASSWORD', 'EXTERNAL_REFERENCE')),
  external_buyer_id TEXT,

  full_name TEXT,
  document_type TEXT,
  document_number TEXT,
  phone TEXT,

  status TEXT NOT NULL DEFAULT 'ACTIVE'
    CHECK (status IN ('ACTIVE', 'INACTIVE', 'SUSPENDED')),

  last_login_at TIMESTAMPTZ,
  password_changed_at TIMESTAMPTZ DEFAULT NOW(),
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

  PRIMARY KEY (id, client_id),
  UNIQUE (client_id, email),
  UNIQUE (client_id, external_buyer_id),

  CONSTRAINT fk_users_client FOREIGN KEY (client_id) REFERENCES ruta.clients(id),

  CONSTRAINT chk_users_password_consistency CHECK (
    (auth_mode = 'PASSWORD' AND password_hash IS NOT NULL) OR
    (auth_mode = 'EXTERNAL_REFERENCE' AND password_hash IS NULL)
  )
) PARTITION BY LIST (client_id);

COMMENT ON TABLE ruta.users IS
  'Tabla única de usuarios. ADMIN_RUTA tiene client_id = 0.';


