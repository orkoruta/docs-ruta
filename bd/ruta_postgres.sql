-- ============================================================================
-- SECCIÓN 8. API KEYS
-- ============================================================================
-- NOTA: Esta tabla NO se particiona por client_id, a diferencia del resto
-- de tablas multi-tenant del modelo. Razones:
--   1. Volumen muy bajo (limits.max_api_keys_per_client = 5).
--   2. Lookup en cada request entrante por key_id sin conocer aún el client_id
--      → requiere unicidad GLOBAL de key_id, incompatible con
--      PARTITION BY LIST (client_id) en PostgreSQL.
--   3. Al dar de baja un cliente, la limpieza se hace con un DELETE explícito
--      en delete_client_permanently() — instantáneo a este volumen.
-- ============================================================================

CREATE TABLE ruta.client_api_keys (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  client_id BIGINT NOT NULL,
  key_id TEXT UNIQUE NOT NULL,
  secret_hash TEXT NOT NULL,
  scopes TEXT[] NOT NULL,
  name TEXT,
  last_used_at TIMESTAMPTZ,
  last_used_ip INET,
  expires_at TIMESTAMPTZ,
  revoked_at TIMESTAMPTZ,
  revocation_reason TEXT,
  created_by_user_id BIGINT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

  PRIMARY KEY (id),
  CONSTRAINT fk_cak_client FOREIGN KEY (client_id) REFERENCES ruta.clients(id)
);


