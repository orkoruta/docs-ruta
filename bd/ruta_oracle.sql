-- SECCIÓN 5. TABLAS DE AUTENTICACIÓN
-- ============================================================================

CREATE TABLE sessions (
  id                       NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  client_id                NUMBER(19) NOT NULL,
  user_id                  NUMBER(19) NOT NULL,
  refresh_token_hash       VARCHAR2(500) NOT NULL,
  control_view_session_id  NUMBER(19),
  ip_address               VARCHAR2(45),
  user_agent               VARCHAR2(2000),
  issued_at                TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
  last_used_at             TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
  expires_at               TIMESTAMP WITH TIME ZONE NOT NULL,
  revoked_at               TIMESTAMP WITH TIME ZONE,
  revocation_reason        VARCHAR2(200),

  CONSTRAINT fk_sessions_user FOREIGN KEY (user_id, client_id) REFERENCES users(id, client_id)
);


CREATE TABLE control_view_master_passwords (
  user_id                 NUMBER(19) NOT NULL,
  client_id               NUMBER(19) DEFAULT 0 NOT NULL,
  master_password_hash    VARCHAR2(500) NOT NULL,
  can_use_control_view    CHAR(1) DEFAULT 'N' NOT NULL,
  set_at                  TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
  expires_at              TIMESTAMP WITH TIME ZONE,

  CONSTRAINT pk_cvmp PRIMARY KEY (user_id, client_id),
  CONSTRAINT fk_cvmp_user FOREIGN KEY (user_id, client_id) REFERENCES users(id, client_id),
  CONSTRAINT chk_cvmp_admin_ruta CHECK (client_id = 0),
  CONSTRAINT chk_cvmp_can_use CHECK (can_use_control_view IN ('Y', 'N'))
);


CREATE TABLE control_view_sessions (
  id                       NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  admin_ruta_user_id       NUMBER(19) NOT NULL,
  impersonated_client_id   NUMBER(19) NOT NULL,
  started_at               TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
  ended_at                 TIMESTAMP WITH TIME ZONE,
  reason                   VARCHAR2(2000),
  ip_address               VARCHAR2(45),
  user_agent               VARCHAR2(2000),

  CONSTRAINT fk_cvs_client FOREIGN KEY (impersonated_client_id) REFERENCES clients(id)
);

ALTER TABLE sessions
  ADD CONSTRAINT fk_sessions_cvs FOREIGN KEY (control_view_session_id)
    REFERENCES control_view_sessions(id);


