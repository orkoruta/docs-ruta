-- ============================================================================
-- RUTA — MODELO DE DATOS ORACLE
-- ============================================================================
-- Plataforma multi-tenant para administración de operaciones comerciales
-- entre Clientes y Compradores.
--
-- Versión: 1.0
-- Schema/User: RUTA
-- Base: Oracle 12c+
--
-- Este archivo es la traducción del modelo PostgreSQL (ruta_postgres.sql)
-- a sintaxis Oracle, optimizada para importar en Oracle SQL Developer
-- Data Modeler y generar el DER.
--
-- Diferencias respecto a la versión PostgreSQL:
--   - BIGINT          → NUMBER(19)
--   - TEXT            → VARCHAR2(N) o CLOB
--   - TIMESTAMPTZ     → TIMESTAMP WITH TIME ZONE
--   - JSONB           → CLOB (con CHECK IS JSON donde aplica)
--   - BOOLEAN         → CHAR(1) con CHECK ('Y','N')
--   - INET            → VARCHAR2(45)
--   - TEXT[]          → VARCHAR2(N) (serializado por la aplicación)
--   - Particionamiento, RLS, stored procedures y triggers complejos
--     están en la versión PostgreSQL; aquí solo se incluye lo necesario
--     para que el DER refleje la estructura completa.
-- ============================================================================


-- ============================================================================
-- SECCIÓN 1. SCHEMA Y TABLESPACE (referencia)
-- ============================================================================
-- Ejecutar como DBA si se desea crear el usuario/schema:
--
-- CREATE TABLESPACE ruta_data DATAFILE 'ruta_data01.dbf' SIZE 500M AUTOEXTEND ON;
-- CREATE USER ruta IDENTIFIED BY <password>
--   DEFAULT TABLESPACE ruta_data
--   QUOTA UNLIMITED ON ruta_data;
-- GRANT CONNECT, RESOURCE, CREATE VIEW, CREATE PROCEDURE, CREATE TRIGGER TO ruta;
--
-- Las tablas a continuación asumen que el usuario actual es RUTA.


-- ============================================================================
-- SECCIÓN 2. TABLA: clients
-- ============================================================================

CREATE TABLE clients (
  id                              NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  business_code                   VARCHAR2(100) NOT NULL,
  slug                            VARCHAR2(200) NOT NULL,
  name                            VARCHAR2(500) NOT NULL,
  description                     VARCHAR2(2000),
  contact_person_name             VARCHAR2(300),
  contact_person_document_type    VARCHAR2(50),
  contact_person_document_number  VARCHAR2(100),
  contact_person_phone            VARCHAR2(50),
  contact_person_email            VARCHAR2(300),
  logo_url                        VARCHAR2(2000),
  client_type                     VARCHAR2(20) NOT NULL,
  frontend_mode                   VARCHAR2(40),
  status                          VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL,
  created_at                      TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
  updated_at                      TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,

  CONSTRAINT uk_clients_business_code UNIQUE (business_code),
  CONSTRAINT uk_clients_slug UNIQUE (slug),
  CONSTRAINT chk_clients_type CHECK (client_type IN ('API', 'FULL')),
  CONSTRAINT chk_clients_frontend_mode CHECK (
    frontend_mode IS NULL OR frontend_mode IN ('NATIVE_RUTA', 'CUSTOM_LANDING_BY_RUTA')
  ),
  CONSTRAINT chk_clients_status CHECK (status IN ('ACTIVE', 'INACTIVE')),
  CONSTRAINT chk_clients_frontend_type CHECK (
    (client_type = 'API' AND frontend_mode IS NULL) OR
    (client_type = 'FULL' AND frontend_mode IS NOT NULL)
  )
);

COMMENT ON TABLE clients IS 'Tenants de la plataforma. client_id=0 es el cliente plataforma (ADMIN_RUTA).';


-- ============================================================================
-- SECCIÓN 3. TABLA: users
-- ============================================================================

CREATE TABLE users (
  id                       NUMBER(19) GENERATED BY DEFAULT AS IDENTITY,
  client_id                NUMBER(19) NOT NULL,
  user_type                VARCHAR2(30) NOT NULL,
  email                    VARCHAR2(300) NOT NULL,
  password_hash            VARCHAR2(500),
  auth_mode                VARCHAR2(30) DEFAULT 'PASSWORD' NOT NULL,
  external_buyer_id        VARCHAR2(200),
  full_name                VARCHAR2(300),
  document_type            VARCHAR2(50),
  document_number          VARCHAR2(100),
  phone                    VARCHAR2(50),
  status                   VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL,
  last_login_at            TIMESTAMP WITH TIME ZONE,
  password_changed_at      TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP,
  created_at               TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
  updated_at               TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,

  CONSTRAINT pk_users PRIMARY KEY (id, client_id),
  CONSTRAINT uk_users_client_email UNIQUE (client_id, email),
  CONSTRAINT uk_users_client_external UNIQUE (client_id, external_buyer_id),
  CONSTRAINT fk_users_client FOREIGN KEY (client_id) REFERENCES clients(id),
  CONSTRAINT chk_users_type CHECK (
    user_type IN ('ADMIN_RUTA', 'ADMIN_CLIENT', 'OPERATOR_CLIENT', 'BUYER', 'COURIER')
  ),
  CONSTRAINT chk_users_auth_mode CHECK (auth_mode IN ('PASSWORD', 'EXTERNAL_REFERENCE')),
  CONSTRAINT chk_users_status CHECK (status IN ('ACTIVE', 'INACTIVE', 'SUSPENDED')),
  CONSTRAINT chk_users_password_consistency CHECK (
    (auth_mode = 'PASSWORD' AND password_hash IS NOT NULL) OR
    (auth_mode = 'EXTERNAL_REFERENCE' AND password_hash IS NULL)
  )
);


