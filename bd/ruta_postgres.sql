-- Parámetros de negocio configurables por Cliente
-- (con override sobre los defaults globales de client_id = 0)
CREATE TABLE ruta.client_parameters (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  client_id BIGINT NOT NULL,
  parameter_key TEXT NOT NULL,
  parameter_value TEXT NOT NULL,
  value_type TEXT NOT NULL
    CHECK (value_type IN ('INTEGER', 'DECIMAL', 'STRING', 'BOOLEAN', 'JSON')),
  description TEXT,
  is_overrideable_by_client BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

  PRIMARY KEY (id, client_id),
  UNIQUE (client_id, parameter_key),
  CONSTRAINT fk_cparam_client FOREIGN KEY (client_id) REFERENCES ruta.clients(id)
) PARTITION BY LIST (client_id);

COMMENT ON TABLE ruta.client_parameters IS
  'Parámetros operativos. Valores globales viven en client_id=0;
  cualquier Cliente puede override individualmente si
  is_overrideable_by_client = TRUE.';


-- Permisos granulares para OPERATOR_CLIENT
CREATE TABLE ruta.operator_permissions (
  operator_user_id BIGINT NOT NULL,
  client_id BIGINT NOT NULL,
  permission_key TEXT NOT NULL,
  granted_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  granted_by_user_id BIGINT NOT NULL,

  PRIMARY KEY (operator_user_id, client_id, permission_key),
  CONSTRAINT fk_op_user FOREIGN KEY (operator_user_id, client_id)
    REFERENCES ruta.users(id, client_id),
  CONSTRAINT fk_op_granter FOREIGN KEY (granted_by_user_id, client_id)
    REFERENCES ruta.users(id, client_id)
) PARTITION BY LIST (client_id);

COMMENT ON TABLE ruta.operator_permissions IS
  'Permisos granulares por OPERATOR_CLIENT. ADMIN_CLIENT y
  ADMIN_RUTA tienen todos los permisos implícitamente; esta tabla
  solo registra los explícitos otorgados a OPERATOR_CLIENT.';


