-- Cancelar pedidos activos cuando se desactiva un Cliente
CREATE OR REPLACE FUNCTION ruta.cancel_client_active_orders(
  p_client_id BIGINT,
  p_admin_user_id BIGINT
)
RETURNS INT AS $$
DECLARE
  cancelled_count INT;
BEGIN
  WITH cancelled AS (
    UPDATE ruta.orders
    SET order_status = 'CANCELLED_BY_ADMIN',
        closure_reason = 'CLIENT_DEACTIVATED',
        closed_at = NOW(),
        refund_status = CASE
          WHEN payment_status = 'PAID' AND order_origin != 'API_LOGISTICS'
            THEN 'REFUND_PENDING'
          ELSE refund_status
        END
    WHERE client_id = p_client_id
      AND order_status NOT IN ('CLOSED', 'COMPLETED_SUCCESSFULLY')
    RETURNING 1
  )
  SELECT COUNT(*) INTO cancelled_count FROM cancelled;

  INSERT INTO ruta.audit_events (
    client_id, actor_user_id, actor_type, action, entity_type, entity_id, result, metadata
  ) VALUES (
    p_client_id, p_admin_user_id, 'ADMIN_RUTA',
    'CLIENT_DEACTIVATION_CASCADE_CANCEL', 'client', p_client_id, 'SUCCESS',
    jsonb_build_object('cancelled_count', cancelled_count)
  );

  RETURN cancelled_count;
END;
$$ LANGUAGE plpgsql;


-- Eliminar definitivamente datos de un Cliente (hard delete)
CREATE OR REPLACE FUNCTION ruta.purge_client_data(
  p_client_id BIGINT,
  p_admin_user_id BIGINT
)
RETURNS VOID AS $$
DECLARE
  client_status TEXT;
  partitioned_tables TEXT[] := ARRAY[
    'users', 'buyer_profiles', 'courier_profiles',
    'client_payment_providers',
    'pickup_points', 'product_categories', 'products',
    'recurrence_templates', 'orders', 'order_items',
    'order_state_history', 'payments', 'refunds',
    'returns', 'disputes', 'webhook_deliveries',
    'idempotency_keys',
    'client_parameters', 'operator_permissions'
  ];
  t TEXT;
BEGIN
  -- Validar que el Cliente esté INACTIVE
  SELECT status INTO client_status FROM ruta.clients WHERE id = p_client_id;
  IF client_status IS NULL THEN
    RAISE EXCEPTION 'Cliente % no existe', p_client_id;
  END IF;
  IF client_status != 'INACTIVE' THEN
    RAISE EXCEPTION 'Cliente % debe estar INACTIVE para ser eliminado', p_client_id;
  END IF;
  IF p_client_id = 0 THEN
    RAISE EXCEPTION 'No se puede eliminar el cliente plataforma (id=0)';
  END IF;

  -- Auditar la acción (en audit_events del Cliente plataforma)
  INSERT INTO ruta.audit_events (
    client_id, actor_user_id, actor_type, action, entity_type, entity_id, result, metadata
  ) VALUES (
    0, p_admin_user_id, 'ADMIN_RUTA',
    'CLIENT_PERMANENT_DELETION', 'client', p_client_id, 'SUCCESS',
    jsonb_build_object('purged_at', NOW())
  );

  -- Drop de todas las particiones del Cliente
  FOREACH t IN ARRAY partitioned_tables LOOP
    EXECUTE format('DROP TABLE IF EXISTS ruta.%I_p%s CASCADE', t, p_client_id);
  END LOOP;

  -- Tablas multi-tenant NO particionadas (ver SECCIONES 8 y 20). Se borran
  -- explícitamente. Orden importante: webhook_subscriptions después del DROP
  -- de la partición de webhook_deliveries (que tiene FK hacia ella).
  DELETE FROM ruta.client_api_keys      WHERE client_id = p_client_id;
  DELETE FROM ruta.webhook_subscriptions WHERE client_id = p_client_id;

  -- NOTA: audit_events del Cliente eliminado se conservan en su partición
  -- por requerimiento legal. NO se elimina la partición de audit_events.

  -- Eliminar el registro del cliente
  DELETE FROM ruta.clients WHERE id = p_client_id;
END;
$$ LANGUAGE plpgsql;


-- Limpieza de idempotency_keys expirados (job de mantenimiento)
CREATE OR REPLACE FUNCTION ruta.cleanup_expired_idempotency_keys()
RETURNS INT AS $$
DECLARE
  deleted_count INT;
BEGIN
  WITH deleted AS (
    DELETE FROM ruta.idempotency_keys
    WHERE expires_at < NOW()
    RETURNING 1
  )
  SELECT COUNT(*) INTO deleted_count FROM deleted;
  RETURN deleted_count;
END;
$$ LANGUAGE plpgsql;


-- Limpieza de sesiones expiradas o revocadas hace más de 30 días
CREATE OR REPLACE FUNCTION ruta.cleanup_expired_sessions()
RETURNS INT AS $$
DECLARE
  deleted_count INT;
BEGIN
  WITH deleted AS (
    DELETE FROM ruta.sessions
    WHERE (revoked_at IS NOT NULL AND revoked_at < NOW() - INTERVAL '30 days')
       OR (expires_at < NOW() - INTERVAL '30 days')
    RETURNING 1
  )
  SELECT COUNT(*) INTO deleted_count FROM deleted;
  RETURN deleted_count;
END;
$$ LANGUAGE plpgsql;


-- Revocar todas las sesiones activas de un usuario
CREATE OR REPLACE FUNCTION ruta.revoke_user_sessions(
  p_client_id BIGINT,
  p_user_id BIGINT,
  p_reason TEXT
)
RETURNS INT AS $$
DECLARE
  revoked_count INT;
BEGIN
  WITH revoked AS (
    UPDATE ruta.sessions
    SET revoked_at = NOW(),
        revocation_reason = p_reason
    WHERE client_id = p_client_id
      AND user_id = p_user_id
      AND revoked_at IS NULL
    RETURNING 1
  )
  SELECT COUNT(*) INTO revoked_count FROM revoked;
  RETURN revoked_count;
END;
$$ LANGUAGE plpgsql;


